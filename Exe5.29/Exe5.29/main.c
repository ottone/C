#include <stdio.h>
int gcd(int , int);

int main(int argc, const char * argv[]) {
    // insert code here...
    int number1, number2;
    
    printf("\nInserisci due numeri interi: ");
    scanf("%d%d",&number1,&number2);
    
    printf("MCD : %d",gcd(number1,number2));
    
    printf("\nHello, World!\n");
    return 0;
}
int gcd(int number1 , int number2){
    int count1 = 1 , count = 0;
    
    while (count1 <= number1 || count1 <= number2){
        if(number1 % count1 == 0 && number2 % count1 == 0)
            count = count1;
        count1++;
    }
    return count;
}