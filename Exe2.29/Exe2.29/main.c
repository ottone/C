/* SCRIVETE UN PROGRAMMA CHE VISUALIZZI GLI INTERI EQUIVALENTI AD ALCUNE LETTERE MAIUSCOLE, MINUSCOLE, NUMERI E SIMBOLI SPECIALI. DETERMINATE, COME MINIMO L'INTERO EQUIVALENTE DI: A B C a b c 0 1 2 $ * + / E DEL CARATTERE SPAZIO.
 
*/


#include <stdio.h>

int main(int argc, const char * argv[]) {
    
    printf("Valore intero del carattere A : %d\n", 'A');
    printf("Valore intero del carattere B : %d\n", 'B');
    printf("Valore intero del carattere C : %d\n", 'C');
    printf("Valore intero del carattere a : %d\n", 'a');
    printf("Valore intero del carattere b : %d\n", 'b');
    printf("Valore intero del carattere c : %d\n", 'c');
    printf("Valore intero del carattere 0 : %d\n", '0');
    printf("Valore intero del carattere 1 : %d\n", '1');
    printf("Valore intero del carattere 2 : %d\n", '2');
    printf("Valore intero del carattere $ : %d\n", '$');
    printf("Valore intero del carattere * : %d\n", '*');
    printf("Valore intero del carattere + : %d\n", '+');
    printf("Valore intero del carattere / : %d\n", '/');
    printf("Valore intero del carattere spazio : %d\n", ' ');
    
    return 0;
}
