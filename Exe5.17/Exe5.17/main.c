#include <stdio.h>

int multiple(int, int);

int main(int argc, const char * argv[]) {
    // insert code here...
    int x = 0, y = 0;
    
    do{
        do{
            printf("\nImmetti due interi positivi EOF per terminare: ");
            scanf("%d%d",&x,&y);
        }while(x <= 0 && y <= 0);
    
    if (x && y)
        multiple(x,y) ? printf("\n%d è multiplo di %d",x,y) : printf("\n%d non è multiplo di %d",x,y);
    
    }while(getchar() != EOF);
    
    printf("\nHello, World!\n");
    return 0;
}

int multiple(int x, int y){
    if (!(x % y))
        return 1;
    else
        return 0;
}