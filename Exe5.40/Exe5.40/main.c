#include <stdio.h>

int main(int argc, const char * argv[]) {
    // insert code here...
    int uno = 1 , due = 2, tre = 3, number;
    int n1, n2 = 0, n3 = 0;
    
    
    printf("Inserisci il numero dei dischi: ");
    scanf("%d",&number);
    
    for(n1 = number ; n3 != number;){
        
        if((n1 == 1 && n2 == 0) || (n1 != 0 && n2 != 0 && n3 == 0) || n1 == number){
            printf("\n%d -> %d",uno, tre);
            n1--;
            n3++;
        }else if(n2 == 0 && n3 != 0 && n1 != 0){
            printf("\n%d -> %d",uno, due);
            n1--;
            n2++;
        }else if(n2 != 0 && n3 != 0 && n1 != 0){
            printf("\n%d -> %d",tre , due);
            n2++;
            n3--;
        }else if(n1 == 0 && n2 != 0 && n3 != 0){
            printf("\n%d -> %d",due , uno);
            n2--;
            n1++;
            printf("\n%d -> %d",due , tre);
            n2--;
            n3++;
        }
    }
    printf("\nHello, World!\n");
    return 0;
}
